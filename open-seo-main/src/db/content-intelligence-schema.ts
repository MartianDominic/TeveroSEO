// ===== Index Coverage =====

export const pageIndexStatus = pgTable(
  "page_index_status",
  {
    id: uuid("id").primaryKey().defaultRandom(),
    siteId: text("site_id")
      .notNull()
      .references(() => siteConnections.id, { onDelete: "cascade" }),
    pageUrl: text("page_url").notNull(),
    // Coverage states from GSC URL Inspection API
    coverageState: text("coverage_state"), // 'Submitted and indexed', 'Crawled - currently not indexed', 'Discovered - currently not indexed', 'URL is unknown to Google', 'Page with redirect', 'Not found (404)', 'Blocked by robots.txt', 'Blocked by noindex', etc.
    indexingState: text("indexing_state"), // 'Indexed', 'Not indexed'
    lastCrawlTime: timestamp("last_crawl_time", { withTimezone: true }),
    crawledAs: text("crawled_as"), // 'Googlebot Desktop', 'Googlebot Smartphone'
    robotsTxtState: text("robots_txt_state"), // 'ALLOWED', 'DISALLOWED', 'UNKNOWN'
    canonicalUrl: text("canonical_url"),
    isCanonical: boolean("is_canonical"),
    mobileUsability: text("mobile_usability"), // 'MOBILE_FRIENDLY', 'NOT_MOBILE_FRIENDLY', 'MOBILE_USABILITY_UNSPECIFIED'
    richResults: jsonb("rich_results").$type<{
      detected: string[];
      items?: unknown[];
    }>(),
    // Additional inspection metadata
    userDeclaredCanonical: text("user_declared_canonical"),
    googleSelectedCanonical: text("google_selected_canonical"),
    pageFetchState: text("page_fetch_state"), // 'SUCCESSFUL', 'SOFT_404', 'BLOCKED_ROBOTS_TXT', 'NOT_FOUND', 'ACCESS_DENIED', 'SERVER_ERROR', 'REDIRECT_ERROR', 'ACCESS_FORBIDDEN', 'BLOCKED_4XX', 'INTERNAL_CRAWL_ERROR', 'INVALID_URL'
    referringUrls: jsonb("referring_urls").$type<string[]>(),
    // Timestamps
    inspectionTime: timestamp("inspection_time", {
      withTimezone: true,
    }).notNull(),
    createdAt: timestamp("created_at", { withTimezone: true }).defaultNow(),
    updatedAt: timestamp("updated_at", { withTimezone: true }).defaultNow(),
    softDeletedAt: timestamp("soft_deleted_at", { withTimezone: true }),
  },
  (table) => [
    unique("uq_page_index_status").on(table.siteId, table.pageUrl),
    index("idx_page_index_coverage_state").on(table.siteId, table.coverageState),
    index("idx_page_index_indexing_state").on(table.siteId, table.indexingState),
    index("idx_page_index_crawl_time").on(table.siteId, table.lastCrawlTime),
    index("idx_page_index_soft_deleted").on(table.softDeletedAt),
  ]
);

export const indexingRequests = pgTable(
  "indexing_requests",
  {
    id: uuid("id").primaryKey().defaultRandom(),
    siteId: text("site_id")
      .notNull()
      .references(() => siteConnections.id, { onDelete: "cascade" }),
    pageUrl: text("page_url").notNull(),
    requestType: text("request_type").notNull(), // 'URL_UPDATED', 'URL_DELETED'
    status: text("status").notNull().default("pending"), // 'pending', 'submitted', 'success', 'failed', 'quota_exceeded'
    priority: integer("priority").default(0), // Higher = more urgent
    priorityReason: text("priority_reason"), // 'new_content', 'decaying', 'high_value', 'manual'
    submittedAt: timestamp("submitted_at", { withTimezone: true }),
    response: jsonb("response").$type<{
      notificationType?: string;
      urlNotificationMetadata?: unknown;
      error?: string;
    }>(),
    errorMessage: text("error_message"),
    retryCount: integer("retry_count").default(0),
    createdAt: timestamp("created_at", { withTimezone: true }).defaultNow(),
    updatedAt: timestamp("updated_at", { withTimezone: true }).defaultNow(),
    softDeletedAt: timestamp("soft_deleted_at", { withTimezone: true }),
  },
  (table) => [
    index("idx_indexing_requests_site_status").on(table.siteId, table.status),
    index("idx_indexing_requests_priority").on(table.priority),
    index("idx_indexing_requests_submitted").on(table.submittedAt),
    index("idx_indexing_requests_soft_deleted").on(table.softDeletedAt),
  ]
);

// ===== Annotations =====
// Phase 96-03: Timeline annotations for analytics charts

export const annotations = pgTable(
  "analytics_annotations",
  {
    id: uuid("id").primaryKey().defaultRandom(),
    siteId: text("site_id").references(() => siteConnections.id, {
      onDelete: "cascade",
    }), // null = workspace-wide annotation
    workspaceId: text("workspace_id")
      .notNull()
      .references(() => organization.id, { onDelete: "cascade" }),
    annotationDate: text("annotation_date").notNull(), // YYYY-MM-DD format
    annotationType: text("annotation_type").notNull(), // 'algorithm_update', 'site_change', 'marketing_campaign', 'technical_issue', 'other'
    title: text("title").notNull(),
    description: text("description"),
    category: text("category"), // 'algorithm_update', 'site_change', 'marketing_campaign', 'technical_issue', 'other'
    color: text("color"), // Hex color for chart display (e.g., "#FF5733")
    impact: text("impact"), // 'positive', 'negative', 'neutral', 'unknown'
    autoGenerated: boolean("auto_generated").default(false),
    sourceUrl: text("source_url"),
    createdBy: text("created_by"),
    createdAt: timestamp("created_at", { withTimezone: true }).defaultNow(),
    updatedAt: timestamp("updated_at", { withTimezone: true }).defaultNow(),
    softDeletedAt: timestamp("soft_deleted_at", { withTimezone: true }),
  },
  (table) => [
    unique("uq_annotations_workspace_date_title").on(
      table.workspaceId,
      table.annotationDate,
      table.title
    ),
    index("idx_annotations_workspace").on(table.workspaceId),
    index("idx_annotations_site").on(table.siteId),
    index("idx_annotations_date").on(table.annotationDate),
    index("idx_annotations_type").on(table.annotationType),
    index("idx_annotations_soft_deleted").on(table.softDeletedAt),
  ]
);

// ===== Google Algorithm Updates =====
// Phase 96: Auto-imported from DemandSphere API for correlation with traffic changes

export const googleAlgorithmUpdates = pgTable(
  "google_algorithm_updates",
  {
    id: uuid("id").primaryKey().defaultRandom(),
    name: text("name").notNull(), // e.g., "March 2024 Core Update"
    updateType: text("update_type").notNull(), // 'core', 'spam', 'helpful_content', 'product_reviews', 'link_spam'
    startedAt: timestamp("started_at", { withTimezone: true }).notNull(),
    endedAt: timestamp("ended_at", { withTimezone: true }), // null if still rolling
    severity: text("severity").notNull(), // 'major', 'minor', 'refresh'
    officialAnnouncementUrl: text("official_announcement_url"),
    notes: text("notes"),
    createdAt: timestamp("created_at", { withTimezone: true }).defaultNow(),
    updatedAt: timestamp("updated_at", { withTimezone: true }).defaultNow(),
  },
  (table) => [
    index("idx_algorithm_updates_started_at").on(table.startedAt),
    index("idx_algorithm_updates_type").on(table.updateType),
    index("idx_algorithm_updates_severity").on(table.severity),
  ]
);

