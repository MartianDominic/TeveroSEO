/**
 * Content Intelligence Schema
 * Phase 96-04: Content Groups, Topic Clusters, Index Coverage
 *
 * Design decisions:
 * - content_groups: folder-based, regex, and manual grouping types
 * - topic_clusters: hub + spoke relationship with coverage tracking
 * - page_index_status: URL Inspection API results with all Google fields
 * - indexing_requests: Track batch indexing with status and response
 */
import {
  pgTable,
  uuid,
  text,
  boolean,
  timestamp,
  jsonb,
  integer,
  real,
  index,
  unique,
} from "drizzle-orm/pg-core";
import { siteConnections } from "./connection-schema";
import { organization } from "./user-schema";

// ===== Content Groups =====

export const contentGroups = pgTable(
  "content_groups",
  {
    id: uuid("id").primaryKey().defaultRandom(),
    siteId: text("site_id")
      .notNull()
      .references(() => siteConnections.id, { onDelete: "cascade" }),
    name: text("name").notNull(),
    description: text("description"),
    matchType: text("match_type").notNull(), // 'folder', 'regex', 'manual'
    matchPattern: text("match_pattern"), // e.g., '/blog/' or '^/products/.*'
    color: text("color"), // Hex color for UI
    isAutoGenerated: boolean("is_auto_generated").default(false),
    createdAt: timestamp("created_at", { withTimezone: true }).defaultNow(),
    updatedAt: timestamp("updated_at", { withTimezone: true }).defaultNow(),
    softDeletedAt: timestamp("soft_deleted_at", { withTimezone: true }),
  },
  (table) => [
    unique("uq_content_groups_site_name").on(table.siteId, table.name),
    index("idx_content_groups_site").on(table.siteId),
    index("idx_content_groups_soft_deleted").on(table.softDeletedAt),
  ]
);

export const contentGroupPages = pgTable(
  "content_group_pages",
  {
    id: uuid("id").primaryKey().defaultRandom(),
    groupId: uuid("group_id")
      .notNull()
      .references(() => contentGroups.id, { onDelete: "cascade" }),
    pageUrl: text("page_url").notNull(),
    manuallyAdded: boolean("manually_added").default(false),
    createdAt: timestamp("created_at", { withTimezone: true }).defaultNow(),
  },
  (table) => [
    unique("uq_content_group_pages").on(table.groupId, table.pageUrl),
    index("idx_content_group_pages_group").on(table.groupId),
    index("idx_content_group_pages_url").on(table.pageUrl),
  ]
);

